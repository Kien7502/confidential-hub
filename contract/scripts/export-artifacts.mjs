// Reads compiled Hardhat artifacts and writes a typed TS module the frontend
// imports directly, so the app never depends on the contract build directory.
import { readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const contractRoot = resolve(here, "..");
const outFile = resolve(contractRoot, "../frontend/src/config/artifacts.ts");

const targets = [
  { export: "PUBLIC_ERC20_ARTIFACT", path: "artifacts/contracts/PublicERC20.sol/PublicERC20.json" },
  { export: "STANDALONE_CERC7984_ARTIFACT", path: "artifacts/contracts/StandaloneCERC7984.sol/StandaloneCERC7984.json" },
  { export: "CERC20_WRAPPER_ARTIFACT", path: "artifacts/contracts/CERC20Wrapper.sol/CERC20Wrapper.json" }
];

const blocks = [];
for (const target of targets) {
  const raw = JSON.parse(await readFile(resolve(contractRoot, target.path), "utf8"));
  const artifact = { abi: raw.abi, bytecode: raw.bytecode };
  blocks.push(`export const ${target.export} = ${JSON.stringify(artifact)} as const;`);
}

const header = "// AUTO-GENERATED by contract/scripts/export-artifacts.mjs. Do not edit by hand.\n";
await writeFile(outFile, header + blocks.join("\n\n") + "\n");
console.log(`Wrote ${targets.length} artifacts to ${outFile}`);
